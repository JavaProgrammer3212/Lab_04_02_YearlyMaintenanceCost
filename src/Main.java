public class Main {
    public static void main(String[] args) {

        int springCosts = 415;

        int summerCosts = 212;

        int fallCosts = 450;

        int winterCosts = 1200;

        int totalCosts;

        System.out.println("The maintenance costs for the Spring season were $" + springCosts);

        System.out.println("The maintenance costs for the Summer season were $" + summerCosts);

        System.out.println("The maintenance costs for the Fall season were $" + fallCosts);

        System.out.println("The maintenance costs for the Winter season were $" + winterCosts);

        totalCosts = springCosts + summerCosts + fallCosts + winterCosts;

        System.out.println("The maintenance costs for the whole year was $" + totalCosts);

    }
}